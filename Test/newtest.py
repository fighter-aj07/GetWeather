def add(a, b):
    return a + b

if __name__ == "__main__":
    print("Testing add function:")
    print(f"2 + 3 = {add(2, 3)}")
    print(f"5 + 7 = {add(5, 7)}")

def subtract(a, b):
    return a - b

if __name__ == "__main__":
    print("Testing subtract function:")
    print(f"2 - 3 = {subtract(2, 3)}")
    print(f"5 - 7 = {subtract(5, 7)}")